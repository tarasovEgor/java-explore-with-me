DROP SEQUENCE IF EXISTS request_data_sequence;

DROP TABLE IF EXISTS request_data CASCADE;

CREATE SEQUENCE IF NOT EXISTS request_data_sequence
    START WITH 1
    INCREMENT BY 1
    CACHE 25;

CREATE TABLE IF NOT EXISTS request_data(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    app VARCHAR(255) NOT NULL,
    uri VARCHAR(255) NOT NULL,
    ip VARCHAR(255) NOT NULL,
    requested_on VARCHAR(255)
);
